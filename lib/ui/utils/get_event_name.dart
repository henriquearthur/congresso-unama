String getEventName(event) {
  if (event == "arquitetura") {
    return "3º Congresso de Arquitetura e Urbanismo";
  } else if (event == "computacao") {
    return "3º Congresso de Informática";
  } else if (event == "engenharia") {
    return "3º Congresso de Engenharia";
  }

  return "3º Congresso de Exatas";
}
